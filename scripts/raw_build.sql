COPY INTO QUICKSTARTS_DB.PLAYER_360.SUPPORT_TICKETS
FROM @QUICKSTARTS_DB.PLAYER_360.SUPPORT/support_tickets.csv 
FILE_FORMAT = (TYPE = 'CSV', SKIP_HEADER = 1);

-- Assign the value of DATEDIFF to the variable
SET days_since_max_log_out = (
    SELECT DATEDIFF(day, MAX(LOG_OUT), CURRENT_DATE) 
    FROM QUICKSTARTS_DB.PLAYER_360.SESSIONS
);

-- Update the days
UPDATE QUICKSTARTS_DB.PLAYER_360.SESSIONS s
SET 
    LOG_IN = DATEADD(DAY, (SELECT $days_since_max_log_out), s.LOG_IN),
    LOG_OUT = DATEADD(DAY, (SELECT $days_since_max_log_out), s.LOG_OUT);

UPDATE QUICKSTARTS_DB.PLAYER_360.PURCHASES p
SET TIMESTAMP_OF_PURCHASE = DATEADD(DAY, (SELECT $days_since_max_log_out), p.TIMESTAMP_OF_PURCHASE);
